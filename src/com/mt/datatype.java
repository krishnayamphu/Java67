package com.mt;

public class datatype {
    public static void main(String[] args) {
        int a=5;
        long b=10L;
        float c=10.5f;
        double d=100.25d;
        boolean bool=true;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(bool);
    }
}
